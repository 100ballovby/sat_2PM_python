import string


print(string.ascii_uppercase)  # все буквы английского алфавита капс
print(string.ascii_lowercase)  # все буквы английского алфавита маленькие
print(string.ascii_letters)  # все буквы английского алфавита
print(string.digits)  # цифры
print(string.hexdigits)  # шестнадцатиричные цифры
print(string.punctuation)  # все спец символы
print(string.printable)  # все, что можно напечатать клавиатурой


