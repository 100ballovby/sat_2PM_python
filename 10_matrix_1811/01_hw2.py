matrix = [
    [1, 5, 2, 7, 9],
    [0, 3, 7, 2, 1],
    [6, 3, 0, 1, 7],
    [8, 8, 9, 9, 1],
    [0, 9, 3, 3, 1],
]
q = 5
for i in range(len(matrix)):
    for j in range(len(matrix[i])):
        if matrix[j][i] == 0:  # если в столбце встретился первый ноль
            q -= 1  # отнять от количества столбцов единицу
            break  # выйти из цикла

print(f'Количество ненулевых столбцов матрицы: {q}')


